// Використовуючи конструкцію if..else, напишіть код, який отримує число через prompt, а потім виводить alert:

// 1, якщо значення більше за нуль,

// -1, якщо значення менше нуля,

// 0, якщо значення дорівнює нулю.

// Передбачається, що користувач вводить лише числа.


let a = +prompt('number');
let result;


if (a > 0) {
    result = 1;
} else if (a === 0) {
    result = 0;
} else if (a < 0) {
    result = -1;
}

alert(result);
  